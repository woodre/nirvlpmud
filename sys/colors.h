/*
 * /sys/colors.h
 * A collection of used colors in the mud and their value as produced
 * by LIB_COLOURS->rgbmt2color()
 */
#ifndef _sys_colours_h
#define _sys_colours_h

#define COLOUR_BLACK         0x00000000

#define COLOUR_RED           0x3f000000
#define COLOUR_GREEN         0x00fc0000
#define COLOUR_BLUE          0x0003f000

#define COLOUR_YELLOW        0x3ffc0000
#define COLOUR_PURPLE        0x3f03f000
#define COLOUR_CYAN          0x00fff000

#define COLOUR_WHITE         0x3ffff000

#define COLOUR_BROWN         0x255c0000
#define COLOUR_ORANGE        0x3f995000

#define COLOUR_SILVER        0x00000fc0
#define COLOUR_GOLDEN        0x3ffc0c80

#define COLOUR_TRANSPARENT   0x0000003f

#endif
